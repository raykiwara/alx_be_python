#script that performs addition, subtraction and multiplication on two numbers

number1 = 10
number2 = 5

print("Addition of {:d} and {:d} is {:d}".format(number1, number2, (number1 + number2)))
print("Subtraction of {:d} and {:d} is {:d}".format(number1, number2, (number1 - number2)))
print("Multiplication of {:d} and {:d} is {:d}".format(number1, number2, (number1 * number2)))
